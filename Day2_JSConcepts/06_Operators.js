// Operators in Javascript:

// An operator is a symbol that performs a specific operation 
// on one or more operands (values).

// For example:

// Below are 2 variables declared and assigned values:
var a = 10;
var b = 5;

// Arithmetic Operators on above variables:
var sum = a + b;        // Addition
var difference = a - b; // Subtraction
var product = a * b;    // Multiplication 
var quotient = a / b;   // Division
var remainder = a % b;  // Modulus
var increment = a++;    // Increment
var decrement = b--;    // Decrement

// Comparison Operators on above variables: 
//Note: Return boolean values based on the comparison of a and b.
var isEqual = a == b;               // Equality
var isNotEqual = a != b;            // Inequality
var isStrictEqual = a === b;        // Strict Equality
var isStrictNotEqual = a !== b;     // Strict Inequality
var isGreaterThan = a > b;          // Greater than
var isLessThan = a < b;             // Less than
var isGreaterThanOrEqual = a >= b;  // Greater than or equal to
var isLessThanOrEqual = a <= b;     // Less than or equal to

// Logical Operators on above variables:
var isTrue = a && b;    // Logical AND
var isFalse = a || b;   // Logical OR
var isNot = !a;         // Logical NOT

// Assignment Operators:
var c = a;  // Assignment
c += b;     // Addition assignment
c -= b;     // Subtraction assignment
c *= b;     // Multiplication assignment
c /= b;     // Division assignment
c %= b;     // Modulus assignment