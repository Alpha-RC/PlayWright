// Variables in JavaScript:

// 1. A variable is a container for storing data values. 

// Example of declaring a variable using 'var':

var a = 10;

// var is a keyword used to declare a variable in JavaScript.
// a is an identifier 
// 10 is a numeric literal
// = is the assignment operator
// ';' is basically used to terminate the statement in JavaScript. 
// It indicates the end of a statement or command.


// Here var will create a container for identifier 'a'
// 'a' is the variable name or we can say it is a container name 
// '=' is used to store or assign the value for the variable/identifier 'a'.
// '10' is the actual value whic will be stored in the container

// So basically a variable is a container that holds a value 
// and we can use the variable name (identifier) to access or manipulate that value in 
// our code.

a= 20;
// Here we are reassigning the value of variable 'a' to 20. 
// Now the variable 'a' will hold the value 20 instead of 10.
// 10 will be totally replaced by 20 in the memory location where 'a' is stored.

// We can also declare a variable without assigning a value to it: 
var b;
// Here we have declared a variable 'b' but we have not assigned any value to it.
// So, by default, the value of 'b' will be undefined until we assign a value to it
// later in the code.