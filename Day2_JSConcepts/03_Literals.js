// Literals in JavaScript:

// 1. A literal is a fixed value that is directly written in the code.
// For example:
var myString = "Hello, World!";
// Here, "Hello, World!" is a string literal.

var myNumber = 42;
// Here, 42 is a numeric literal.

var myBoolean = true;
// Here, true is a boolean literal.

var myNull = null;
// Here, null is a null literal.

var myUndefined;
// Here, myUndefined is declared but not assigned a value, so it is undefined.

