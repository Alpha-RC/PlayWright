// Defination of a function
function greet()
{
    console.log("Hello, How are you?");
}

// Calling the function
greet(); // Output: Hello, How are you?

// Function with parameters
// here 'name' is a parameter that the function takes as input
function greetPerson(name) 
{
    console.log("Hello, " + name + "!"); //here we are using the parameter 'name' to personalize the
                                         //greeting message
}

// Calling the function with an argument
greetPerson("Alice"); // Output: Hello, Alice!


// Now we can call this function n number of times
// So something which is reusable and can be called multiple times is called a function.