// Comments in Javascript:

// Comments are used to explain code, make it more readable, and to prevent execution of code when testing.

// There are two types of comments in JavaScript:

// 1. Single-line comments: These comments start with '//' and continue until the end of the line.
// For example:
var a = 10; // This is a single-line comment explaining that 'a' is assigned the value 10

// 2. Multi-line comments: These comments start with '/*' and end with '*/'. They can span multiple lines.

/*
This is a multi-line comment.
It can be used to write comments that span multiple lines.
For example, we can explain a block of code or provide detailed information about a function or variable.
*/

// Comments are ignored by the JavaScript engine and do not affect the execution of the code.
// They are purely for the benefit of developers to understand the code better and to communicate with other
// developers who may read the code in the future.

// Shortcuts for comments in most code editors:
// - To create a single-line comment, you can usually use the shortcut 'Ctrl + /' (Windows)
// - To create a multi-line comment, you can select the block of code and use the same shortcut 'Ctrl + /',
//   and it will toggle between commenting and uncommenting the selected code.